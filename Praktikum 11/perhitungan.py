def tambah(bil1, bil2):
    hasil= bil1 + bil2
    print("Hasil pertambahan", hasil)

def kurang(bil1, bil2):
    hasil= bil1 - bil2
    print("Hasil pengurangan", hasil)

def kali(bil1, bil2):
    hasil= bil1 * bil2
    print("Hasil perkalian", hasil)

def bagi(bil1, bil2):
    hasil= bil1 / bil2
    print("Hasil pembagian", hasil)

def bagi_bulat(bil1, bil2):
    hasil= bil1 // bil2
    print("Hasil pembagian bulat", hasil)

def modulus(bil1, bil2):
    hasil= bil1 % bil2
    print("Hasil modulus", hasil)

def pangkat(bil1, bil2):
    hasil= bil1 ** bil2
    print("Hasil perpangkatan", hasil)

def perkalian_float(bil):
    bil *= 1.5
    print("Hasil perkalian float", bil)

def increment(bil):
    bil += 1
    print("Increment", bil)

def decrement(bil):
    bil -= 1
    print("Decrement", bil)